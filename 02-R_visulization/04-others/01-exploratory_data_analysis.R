# 探索性数据分析

# 目的
#  了解数据特征、找到数据中的模式、形成分析策略
#  图与数字互相验证、帮助发现错误、用于交流结果

# 特点
#  快速、通常呈现在屏幕设备
#  不需要过分注重是否漂亮（影响数据分析，或用户分享，可做的漂亮点）

# 分析性作图的六大原则
#  1. 凸显比较（谁和谁比？）
#  2. 凸显机制（因果/机制/解释）
#  3. 凸显多元性（>2个变量、逃离扁平）
#  4. 整合证据
#   整合文字、数字、图、表等
#   用多种方式显示数据的特征
#   不要让工具主宰分析
#  5. 使用适当的图标、尺度等
#   完整性、一图胜千言
#  6. 内容是王道
#   研究问题的重要性 > 作图漂亮

# 获取帮助
# 如何问问题
#  操作系统、版本、哪一步产生的错误、预期时什么、得到的结果是什么、其他有用的信息
#  例如：Win7 R3.2.0 lm() "seg fault on large data frame"
